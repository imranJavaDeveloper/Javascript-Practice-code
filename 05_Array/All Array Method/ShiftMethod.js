

/*** Example Of shift() */
let Array=["Desktop","Laptop","Ipad Air"]
/** remove first element form the Array */
console.log(Array.shift());// Desktop
console.log(Array); //  ['Laptop', 'Ipad Air']

/** Example 2 */

let Ar=["Mohan","Sohan","Mukesh","Sita"];
/** remove first element form the Array */
console.log(Ar.shift());// Mohan
console.log(Ar); //  ['Sohan', 'Mukesh', 'Sita']



